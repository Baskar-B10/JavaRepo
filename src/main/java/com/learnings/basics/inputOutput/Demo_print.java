package com.learnings.basics.inputOutput;// Java code to illustrate print()
import java.io.*;

class Demo_print {
    public static void main(String[] args)
    {

        // using print()
        // all are printed in the
        // same line
        System.out.print("GfG! ");
        System.out.print("GfG! ");
        System.out.print("GfG! ");
        // using println()
        // all are printed in the
        // different line
        System.out.println("GfG! ");
        System.out.println("GfG! ");
        System.out.println("GfG! ");
    }
}