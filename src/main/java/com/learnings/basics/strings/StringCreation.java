package com.learnings.basics.strings;// Java Program to Create a String

class StringCreation {
    public static void main (String[] args) {
      
          // String literal
        String s1="String1";
          System.out.println(s1);
      
          // Using new Keyword 
          String s2= new String("String2");
          System.out.println(s2);
    }
}